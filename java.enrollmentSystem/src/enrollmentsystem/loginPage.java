    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        // login button
        try
        {
            String username = TFStdID.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            
            String selectedUserType = (String) jComboBox1.getSelectedItem();
            switch (selectedUserType) {
                case "Student":
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    connDB2 = DriverManager.getConnection(url, "root", "$4Y my n4m3");
                    pst = connDB2.prepareStatement("SELECT * FROM `enrollmentsys`.`logindatastudent` WHERE `StudentID` = ? and `Password` = ?");
                    pst.setString(1, username);
                    pst.setString(2, password);
                    rs = pst.executeQuery();
                    
                    if (rs.next())
                    {
                        String name = rs.getString("Name");
                        JOptionPane.showMessageDialog(null, "Login successful.");
                        new StudentFrame(name).setVisible(true);
                        dispose(); // Close the current loginPage frame
                    }else{
                        if (username.isEmpty() && password.isEmpty()){
                        JOptionPane.showMessageDialog(null, "Please fill in all fields", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if(password.equals("")){
                        JOptionPane.showMessageDialog(null, "Password is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if (username.equals("")){
                        JOptionPane.showMessageDialog(null, "Username is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else{
                        JOptionPane.showMessageDialog(null, "Incorrect details. Please try again","Error",  JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    break;
                    
                case "Faculty":
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    connDB3 = DriverManager.getConnection(url, "root", "$4Y my n4m3");
                    pst = connDB3.prepareStatement("SELECT * FROM `enrollmentsys`.`logindatafaculty` WHERE `FacultyID` = ? and `Password` = ?");
                    
                    pst.setString(1, username);
                    pst.setString(2, password);
                    rs = pst.executeQuery();
                    if (rs.next())
                    {
                        String name = rs.getString("Name");
                        JOptionPane.showMessageDialog(null, "Login successful.");
                        new FacultyFrame(name).setVisible(true);
                        dispose(); // Close the current loginPage frame
                    }
                    else
                    {
                        if (username.isEmpty() && password.isEmpty()){
                            JOptionPane.showMessageDialog(null, "Please fill in all fields", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if(password.equals("")){
                            JOptionPane.showMessageDialog(null, "Password is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if (username.equals("")){
                            JOptionPane.showMessageDialog(null, "Username is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else{
                            JOptionPane.showMessageDialog(null, "Incorrect details. Please try again","Error",  JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    break;
                    
                case "Admin":
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    connDB1 = DriverManager.getConnection(url, "root", "$4Y my n4m3");
                    pst = connDB1.prepareStatement("SELECT * FROM `enrollmentsys`.`login_admin` WHERE `Email` = ? and `Password` = ?");
                    
                    pst.setString(1, username);
                    pst.setString(2, password);
                    rs = pst.executeQuery();
                    if (rs.next())
                    {
                        JOptionPane.showMessageDialog(null, "Login successful.");
                        new AdminFrame().setVisible(true);
                        dispose(); // Close the current loginPage frame
                    }else{
                        if (username.isEmpty() && password.isEmpty()){
                        JOptionPane.showMessageDialog(null, "Please fill in all fields", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if(password.equals("")){
                        JOptionPane.showMessageDialog(null, "Password is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else if (username.equals("")){
                        JOptionPane.showMessageDialog(null, "Username is empty.", "Error",JOptionPane.ERROR_MESSAGE);
                        }else{
                        JOptionPane.showMessageDialog(null, "Incorrect details. Please try again","Error",  JOptionPane.ERROR_MESSAGE);
                        }
                    }
                    break;
                    
                default:
                    
                    break;
            }
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

