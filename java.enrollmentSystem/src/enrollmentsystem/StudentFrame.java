    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        try
        {
            String confirm = JOptionPane.showInputDialog("Enter Subject ID: ");
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection(dataCon, username, password);
            pst = conn.prepareStatement("SELECT * FROM `enrollmentsys`.`subject_list` WHERE `SubjectID` = '"+ confirm +"'");
            rs = pst.executeQuery();
            
            if (rs.next())
            {
                String confirm1 = JOptionPane.showInputDialog("Confirm Student ID: ");
                pst = conn.prepareStatement("INSERT INTO `enrollmentsys`.`class1` (StudentID, SName, Section) VALUES (?,?,?)");
                pst.setString(1, confirm1);
                pst.setString(2, JOptionPane.showInputDialog("Enter Full name:"));
                pst.setString(3, JOptionPane.showInputDialog("Enter section:"));
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data added.");
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

